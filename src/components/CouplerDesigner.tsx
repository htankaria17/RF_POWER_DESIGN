        stripWidth = h * 2.2; // Mixed impedances
        stripGap = 0;
        electricalLength = 90; // Quarter wavelength
        couplingValue = -3; // 3dB split
        isolation = 25;
        returnLoss = 25;
        vswr = 1.2;
        bandwidth = 35;
        break;
